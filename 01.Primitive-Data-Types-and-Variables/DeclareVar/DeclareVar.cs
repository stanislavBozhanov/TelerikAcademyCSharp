//Declare variables for following values: 52130, -115, 4825932, 97, -10000

using System;

class DeclareVar
{
    static void Main()
    {
        byte firstVar = 97;
        sbyte secondVar = -115;
        short thirdVar = -10000;
        ushort forthVar = 52130;
        int fifthVar = 4825932;
        Console.Write("The first variable is: {0}\nThe second variable is: {1}\nThe third variable is: {2}\nThe forth variable is: {3}\nThe fifth variable is: {4}\n", firstVar, secondVar, thirdVar, forthVar, fifthVar);
    }
}
